export const getTourById = (id: string): Tour | undefined => tours.find(t => t.id === id);

export const getRelatedTours = (tourId: string, count = 3): Tour[] => {
  const tour = getTourById(tourId);
  if (!tour) return tours.slice(0, count);
  return tours.filter(t => t.id !== tourId && (t.category === tour.category || t.region === tour.region)).slice(0, count);
};
